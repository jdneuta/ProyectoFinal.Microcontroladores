/**
 * @brief Inicializa el display OLED SSD1306 con la secuencia estßndar.
 */
void oled_init(void) {
    __delay_ms(100);            // Espera estabilizaciˇn de alimentaciˇn

    oled_cmd(0xAE);             // Display OFF
    oled_cmd(0xD5); oled_cmd(0x80); // Reloj display
    oled_cmd(0xA8); oled_cmd(0x3F); // Multiplexor 64
    oled_cmd(0xD3); oled_cmd(0x00); // Offset display = 0
    oled_cmd(0x40);             // LÝnea de inicio = 0
    oled_cmd(0x8D); oled_cmd(0x14); // Habilitar charge pump
    oled_cmd(0x20); oled_cmd(0x00); // Modo de direccionamiento horizontal
    oled_cmd(0xA1);             // Seg remap
    oled_cmd(0xC8);             // COM scan direction
    oled_cmd(0xDA); oled_cmd(0x12); // COM pins
    oled_cmd(0x81); oled_cmd(0xCF); // Contraste
    oled_cmd(0xD9); oled_cmd(0xF1); // Pre-charge
    oled_cmd(0xDB); oled_cmd(0x40); // VCOMH
    oled_cmd(0xA4);             // Display desde RAM
    oled_cmd(0xA6);             // Display normal (no invertido)
    oled_cmd(0xAF);             // Display ON

    oled_limpiar();
}

/**
 * @brief Limpia toda la pantalla escribiendo ceros en todas las pßginas.
 */
void oled_limpiar(void) {
    for (uint8_t pagina = 0; pagina < OLED_PAGINAS; pagina++) {
        oled_set_cursor(pagina, 0);
        i2c_start();
        i2c_escribir(OLED_DIRECCION << 1);
        i2c_escribir(OLED_DATO);
        for (uint8_t col = 0; col < OLED_ANCHO; col++) {
            i2c_escribir(0x00);
        }
        i2c_stop();
    }
}

/**
 * @brief Posiciona el cursor del SSD1306 en pßgina y columna dadas.
 * @param pagina  Fila (0 - 7).
 * @param columna Columna (0 - 127).
 */
void oled_set_cursor(uint8_t pagina, uint8_t columna) {
    oled_cmd(0xB0 | pagina);                    // Pßgina
    oled_cmd(0x00 | (columna & 0x0F));          // Columna baja
    oled_cmd(0x10 | ((columna >> 4) & 0x0F));   // Columna alta
}

/**
 * @brief Escribe un carßcter ASCII en la posiciˇn actual del cursor.
 * @param c Carßcter a mostrar.
 */
void oled_escribir_char(char c) {
    if (c < 32 || c > 126) c = ' ';

    uint8_t idx = (uint8_t)(c - 32);

    i2c_start();
    i2c_escribir(OLED_DIRECCION << 1);
    i2c_escribir(OLED_DATO);
    for (uint8_t col = 0; col < 5; col++) {
        i2c_escribir(fuente_5x8[idx][col]);
    }
    i2c_escribir(0x00);     // Espacio entre caracteres
    i2c_stop();
}

/**
 * @brief Escribe una cadena en una fila del display.
 * @param pagina Fila (0 - 7).
 * @param cadena Cadena terminada en '\0'.
 */
void oled_escribir_linea(uint8_t pagina, const char *cadena) {
    oled_set_cursor(pagina, 0);
    while (*cadena != '\0') {
        oled_escribir_char(*cadena);
        cadena++;
    }
}

/**
 * @brief Muestra FC, temperatura y estado de alarma en el display.
 *
 * Distribuye la informaciˇn en 4 filas:
 * - Fila 0: TÝtulo "Monitor Vital"
 * - Fila 2: "FC: XXX BPM"
 * - Fila 4: "T: XX.X C"
 * - Fila 6: "ALARMA!" o "OK"
 *
 * @param fc       Frecuencia cardÝaca en BPM.
 * @param temp_x10 Temperatura escalada (░C Î 10).
 * @param alarma   true si hay condiciˇn de alarma activa.
 */
void oled_mostrar_vitales(uint8_t fc, int16_t temp_x10, bool alarma) {
    char buffer[16];
    uint8_t i;

    // Fila 0: tÝtulo
    oled_escribir_linea(0, "Monitor Vital");

    // Fila 2: frecuencia cardÝaca
    // Construir "FC: XXX BPM"
    i = 0;
    buffer[i++] = 'F';
    buffer[i++] = 'C';
    buffer[i++] = ':';
    buffer[i++] = ' ';

    // Convertir fc a dÝgitos
    if (fc >= 100) buffer[i++] = '0' + (fc / 100);
    if (fc >= 10)  buffer[i++] = '0' + ((fc / 10) % 10);
    buffer[i++] = '0' + (fc % 10);

    buffer[i++] = ' ';
    buffer[i++] = 'B';
    buffer[i++] = 'P';
    buffer[i++] = 'M';
    buffer[i++] = '\0';
    oled_escribir_linea(2, buffer);

    // Fila 4: temperatura "T: XX.X C"
    i = 0;
    buffer[i++] = 'T';
    buffer[i++] = ':';
    buffer[i++] = ' ';

    int16_t t = temp_x10;
    if (t < 0) { buffer[i++] = '-'; t = -t; }
    uint16_t entero = (uint16_t)(t / 10);
    uint8_t  decimal = (uint8_t)(t % 10);
    if (entero >= 10) buffer[i++] = '0' + (uint8_t)(entero / 10);
    buffer[i++] = '0' + (uint8_t)(entero % 10);
    buffer[i++] = '.';
    buffer[i++] = '0' + decimal;
    buffer[i++] = ' ';
    buffer[i++] = 'C';
    buffer[i++] = '\0';
    oled_escribir_linea(4, buffer);

    // Fila 6: estado
    if (alarma) {
        oled_escribir_linea(6, "** ALARMA! **");
    } else {
        oled_escribir_linea(6, "Estado: OK    ");
    }
}
