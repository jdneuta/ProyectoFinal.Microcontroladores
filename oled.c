/**
 * @brief EnvÝa un byte de comando al SSD1306.
 * @param cmd Comando a enviar.
 */
static void oled_cmd(uint8_t cmd) {
    i2c_start();
    i2c_escribir(OLED_DIRECCION << 1);
    i2c_escribir(OLED_CMD);
    i2c_escribir(cmd);
    i2c_stop();
}

